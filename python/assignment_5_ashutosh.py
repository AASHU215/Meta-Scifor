def count_characters(string):
    upper_count = 0
    lower_count = 0
    digit_count = 0
    symbol_count = 0

    for char in string:
        if char.isupper():
            upper_count += 1
        elif char.islower():
            lower_count += 1
        elif char.isdigit():
            digit_count += 1
        else:
            symbol_count += 1

    return upper_count, lower_count, digit_count, symbol_count

def main():
    string = input("Enter a string: ")
    upper_count, lower_count, digit_count, symbol_count = count_characters(string)

    print("Total number of uppercase letters:", upper_count)
    print("Total number of lowercase letters:", lower_count)
    print("Total number of digits:", digit_count)
    print("Total number of symbols:", symbol_count)

if __name__ == "__main__":
    main()

