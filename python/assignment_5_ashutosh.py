def vending_machine(total_candies_to_load):
    while total_candies_to_load > 0:
        num_candies = int(input("How many candies would you like to purchase? "))

        total_amount = num_candies * 1  # Each candy costs 1 dollar

        amount_paid = float(input("Please enter the amount for the candies ($1 each): $"))

        if amount_paid < total_amount:
            print("Insufficient amount paid. Please insert more money.")
        elif amount_paid == total_amount:
            print(f"Here are your {num_candies} candies. Enjoy!")
            total_candies_to_load -= num_candies
        else:
            change = amount_paid - total_amount
            print(f"Here are your {num_candies} candies. Enjoy! And here's your change: ${change:.2f}")
            total_candies_to_load -= num_candies

    print("Candy vending machine is empty. Please refill.")

def main():
    total_candies_to_load = int(input("How many candies to load onto the vending machine? "))
    vending_machine(total_candies_to_load)

if __name__ == "__main__":
    main()

