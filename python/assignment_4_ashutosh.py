def simple_interest(principal, rate, time):
    return principal * rate * time / 100

def compound_interest(principal, rate, time):
    return principal * ((1 + rate / 100) ** time - 1)

def main():
    principal = 10000
    rate = 6
    time = 30

    yash_return = simple_interest(principal, rate, time)
    vishal_return = compound_interest(principal, rate, time)

    difference = vishal_return - yash_return

    print("Difference in returns after 30 years:")
    print("Yash's return (Simple Interest): $", round(yash_return, 2))
    print("Vishal's return (Compound Interest): $", round(vishal_return, 2))
    print("Difference: $", round(difference, 2))

    # Calculate difference in returns for one year
    yash_return_one_year = simple_interest(principal, rate, 1)
    vishal_return_one_year = compound_interest(principal, rate, 1)
    difference_one_year = vishal_return_one_year - yash_return_one_year

    print("\nDifference in returns for one year:")
    print("Yash's return (Simple Interest) for one year: $", round(yash_return_one_year, 2))
    print("Vishal's return (Compound Interest) for one year: $", round(vishal_return_one_year, 2))
    print("Difference for one year: $", round(difference_one_year, 2))

if __name__ == "__main__":
    main()

