# Initialize bank balance
bank_balance = 50000

# Function to handle withdrawal
def withdraw(amount):
    global bank_balance
    if amount % 100 == 0:
        if amount <= bank_balance:
            bank_balance -= amount
            print(f"Withdrawn {amount} successfully. Remaining balance: {bank_balance}")
        else:
            print("Insufficient funds!")
    else:
        print("Error: Withdraw amount must be in multiples of 100.")

# Function to handle deposit
def deposit(amount):
    global bank_balance
    bank_balance += amount
    print(f"Deposited {amount} successfully. Updated balance: {bank_balance}")

# Function to show balance enquiry
def balance_enquiry():
    print(f"Your current account balance is: {bank_balance}")

# Function to handle fast cash option
def fast_cash(option):
    global bank_balance
    options = [5000, 10000, 15000, 20000, 25000, 30000, 40000, 45000, 50000]
    if option in options:
        if option <= bank_balance:
            bank_balance -= option
            print(f"Withdrawn {option} as fast cash. Remaining balance: {bank_balance}")
        else:
            print("Insufficient funds!")
    else:
        print("Error: Invalid fast cash option.")

# Function to display ATM options
def atm_options():
    print("ATM Machine Options:")
    print("1) Withdraw")
    print("2) Deposit Cash")
    print("3) Balance Enquiry")
    print("4) Fast Cash")

# Main function to drive the ATM machine
def main():
    atm_options()
    choice = int(input("Enter your choice: "))

    if choice == 1:
        amount = int(input("Enter the amount to withdraw: "))
        withdraw(amount)
    elif choice == 2:
        amount = int(input("Enter the amount to deposit: "))
        deposit(amount)
    elif choice == 3:
        balance_enquiry()
    elif choice == 4:
        option = int(input("Select the fast cash option: "))
        fast_cash(option)
    else:
        print("Error: Invalid choice. Please select a valid option.")

if __name__ == "__main__":
    main()