def calculate_tax(annual_income):
    tax = 0
    if annual_income <= 250000:
        tax = 0
    elif 250000 < annual_income <= 500000:
        tax = (annual_income - 250000) * 0.05
    elif 500000 < annual_income <= 1000000:
        tax = 12500 + (annual_income - 500000) * 0.2
    else:
        tax = 112500 + (annual_income - 1000000) * 0.3
    return tax

def main():
    annual_income = float(input("Enter your annual income: "))
    tax = calculate_tax(annual_income)
    print("Tax to be paid: $", round(tax, 2))

if __name__ == "__main__":
    main()

